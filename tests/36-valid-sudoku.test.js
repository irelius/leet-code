import { describe, test, expect } from "vitest";
import { validSudoku } from "../questions/36-valid-sudoku";

describe("36 - Valid Sudoku", () => {
	test("Case 1", () => {
		expect(
			validSudoku([
				["1", "2", ".", ".", "3", ".", ".", ".", "."],
				["4", ".", ".", "5", ".", ".", ".", ".", "."],
				[".", "9", "8", ".", ".", ".", ".", ".", "3"],
				["5", ".", ".", ".", "6", ".", ".", ".", "4"],
				[".", ".", ".", "8", ".", "3", ".", ".", "5"],
				["7", ".", ".", ".", "2", ".", ".", ".", "6"],
				[".", ".", ".", ".", ".", ".", "2", ".", "."],
				[".", ".", ".", "4", "1", "9", ".", ".", "8"],
				[".", ".", ".", ".", "8", ".", ".", "7", "9"],
			])
		).toEqual(true);
	});

	test("Case 2", () => {
		expect(
			validSudoku([
				["1", "2", ".", ".", "3", ".", ".", ".", "."],
				["4", ".", ".", "5", ".", ".", ".", ".", "."],
				[".", "9", "1", ".", ".", ".", ".", ".", "3"],
				["5", ".", ".", ".", "6", ".", ".", ".", "4"],
				[".", ".", ".", "8", ".", "3", ".", ".", "5"],
				["7", ".", ".", ".", "2", ".", ".", ".", "6"],
				[".", ".", ".", ".", ".", ".", "2", ".", "."],
				[".", ".", ".", "4", "1", "9", ".", ".", "8"],
				[".", ".", ".", ".", "8", ".", ".", "7", "9"],
			])
		).toEqual(false);
	});

	test("Case 3", () => {
		expect(
			validSudoku([
				["1", "2", ".", ".", "3", ".", ".", ".", "."],
				["4", ".", ".", "5", ".", ".", ".", ".", "."],
				["1", "9", "1", ".", ".", ".", ".", ".", "3"],
				["5", ".", ".", ".", "6", ".", ".", ".", "4"],
				[".", ".", ".", "8", ".", "3", ".", ".", "5"],
				["7", ".", ".", ".", "2", ".", ".", ".", "6"],
				[".", ".", ".", ".", ".", ".", "2", ".", "."],
				[".", ".", ".", "4", "1", "9", ".", ".", "8"],
				[".", ".", ".", ".", "8", ".", ".", "7", "9"],
			])
		).toEqual(false);
	});
});
