import { describe, expect, test } from "vitest";
import { generateParentheses } from "../questions/22-generate-parentheses/22";

describe("22 - Generate Parentheses", () => {
	test("Case 1", () => {
		generateParentheses(1).toEqual(["()"]);
	});

	test("Case 2", () => {
		generateParentheses(2).toEqual(["(())", "()()"]);
	});

	test("Case 3", () => {
		generateParentheses(3).toEqual(["((()))", "(()())", "(())()", "()(())", "()()()"]);
	});

	test("Case 4", () => {
		generateParentheses(4).toEqual([
			[
				"(((())))",
				"((()()))",
				"((())())",
				"((()))()",
				"(()(()))",
				"(()()())",
				"(()())()",
				"(())(())",
				"(())()()",
				"()((()))",
				"()(()())",
				"()(())()",
				"()()(())",
				"()()()()",
			],
		]);
	});

	test("Case 5", () => {
		generateParentheses(5).toEqual([
			[
				"((((()))))",
				"(((()())))",
				"(((())()))",
				"(((()))())",
				"(((())))()",
				"((()(())))",
				"((()()()))",
				"((()())())",
				"((()()))()",
				"((())(()))",
				"((())()())",
				"((())())()",
				"((()))(())",
				"((()))()()",
				"(()((())))",
				"(()(()()))",
				"(()(())())",
				"(()(()))()",
				"(()()(()))",
				"(()()()())",
				"(()()())()",
				"(()())(())",
				"(()())()()",
				"(())((()))",
				"(())(()())",
				"(())(())()",
				"(())()(())",
				"(())()()()",
				"()(((())))",
				"()((()()))",
				"()((())())",
				"()((()))()",
				"()(()(()))",
				"()(()()())",
				"()(()())()",
				"()(())(())",
				"()(())()()",
				"()()((()))",
				"()()(()())",
				"()()(())()",
				"()()()(())",
				"()()()()()",
			],
		]);
	});
});
